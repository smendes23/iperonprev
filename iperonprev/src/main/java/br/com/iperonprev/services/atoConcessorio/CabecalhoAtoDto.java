package br.com.iperonprev.services.atoConcessorio;

public class CabecalhoAtoDto {

	private String tituloCabecalho;
	private String chefePoder;
	private String tituloChefePoder;
	public String getTituloCabecalho() {
		return tituloCabecalho;
	}
	public void setTituloCabecalho(String tituloCabecalho) {
		this.tituloCabecalho = tituloCabecalho;
	}
	public String getChefePoder() {
		return chefePoder;
	}
	public void setChefePoder(String chefePoder) {
		this.chefePoder = chefePoder;
	}
	public String getTituloChefePoder() {
		return tituloChefePoder;
	}
	public void setTituloChefePoder(String tituloChefePoder) {
		this.tituloChefePoder = tituloChefePoder;
	}
}
