package br.com.iperonprev.util.averbacao;

public class DiaMesAno {

	private int dia;
	private int mes;
	private int ano;
	public int getDia() {
		return dia;
	}
	public int getMes() {
		return mes;
	}
	public int getAno() {
		return ano;
	}
	
	public DiaMesAno() {
	}
	
	public DiaMesAno(int dia, int mes, int ano) {
		super();
		this.dia = dia;
		this.mes = mes;
		this.ano = ano;
	}
	
	
}
